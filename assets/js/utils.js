// –§—É–Ω–∫—Ü–∏—è –¥–ª—è –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏–∏ –¥–∞–Ω–Ω—ã—Ö –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –ø—Ä–∏ –ø–µ—Ä–≤–æ–π –∑–∞–≥—Ä—É–∑–∫–µ
function initUserData() {
    if (!localStorage.getItem('userData')) {
        const initialData = {
            username: "–ì–æ—Å—Ç—å",
            credits: 1000,
            registered: null,
            gamesPlayed: 0
        };
        localStorage.setItem('userData', JSON.stringify(initialData));
    }
    
    // –û–±–Ω–æ–≤–ª—è–µ–º –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏–µ –∫—Ä–µ–¥–∏—Ç–æ–≤ –≤ —Ö–µ–¥–µ—Ä–µ
    updateHeaderCredits();
}

// –ü–æ–ª—É—á–µ–Ω–∏–µ –¥–∞–Ω–Ω—ã—Ö –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –∏–∑ localStorage
function getUserData() {
    const userDataString = localStorage.getItem('userData');
    if (userDataString) {
        return JSON.parse(userDataString);
    }
    return {
        username: "–ì–æ—Å—Ç—å",
        credits: 1000,
        registered: null,
        gamesPlayed: 0
    };
}

// –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ –∫—Ä–µ–¥–∏—Ç–æ–≤
function updateCredits(amount) {
    const userData = getUserData();
    userData.credits = (userData.credits || 1000) + amount;
    
    // –°–æ—Ö—Ä–∞–Ω—è–µ–º –æ–±–Ω–æ–≤–ª–µ–Ω–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ
    localStorage.setItem('userData', JSON.stringify(userData));
    
    // –û–±–Ω–æ–≤–ª—è–µ–º –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏–µ –≤ —Ö–µ–¥–µ—Ä–µ
    updateHeaderCredits();
    
    // –ï—Å–ª–∏ –æ—Ç–∫—Ä—ã—Ç–∞ —Å—Ç—Ä–∞–Ω–∏—Ü–∞ –±–∞–ª–∞–Ω—Å–∞, –æ–±–Ω–æ–≤–ª—è–µ–º –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏–µ —Ç–∞–º
    const balanceAmount = document.querySelector(".balance-amount");
    if (balanceAmount) {
        balanceAmount.textContent = `üí∞ ${userData.credits} –∫—Ä–µ–¥–∏—Ç–æ–≤`;
    }
    
    return userData.credits;
}

// –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è –∫—Ä–µ–¥–∏—Ç–æ–≤ –≤ —Ö–µ–¥–µ—Ä–µ
function updateHeaderCredits() {
    const userData = getUserData();
    
    const creditsElement = document.getElementById("credits");
    if (creditsElement) {
        creditsElement.textContent = `üí∞ ${userData.credits} –∫—Ä–µ–¥–∏—Ç–æ–≤`;
    }
}

// –§—É–Ω–∫—Ü–∏—è –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è —É–≤–µ–¥–æ–º–ª–µ–Ω–∏–π
function showNotification(message, type = "info") {
    // –°–æ–∑–¥–∞–µ–º —ç–ª–µ–º–µ–Ω—Ç —É–≤–µ–¥–æ–º–ª–µ–Ω–∏—è
    const notification = document.createElement("div");
    notification.className = `notification ${type}`;
    notification.textContent = message;
    
    // –î–æ–±–∞–≤–ª—è–µ–º —É–≤–µ–¥–æ–º–ª–µ–Ω–∏–µ –≤ DOM
    document.body.appendChild(notification);
    
    // –ê–Ω–∏–º–∞—Ü–∏—è –ø–æ—è–≤–ª–µ–Ω–∏—è
    setTimeout(() => {
        notification.classList.add("visible");
    }, 10);
    
    // –ê–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–æ–µ —É–¥–∞–ª–µ–Ω–∏–µ —á–µ—Ä–µ–∑ 3 —Å–µ–∫—É–Ω–¥—ã
    setTimeout(() => {
        notification.classList.remove("visible");
        setTimeout(() => {
            document.body.removeChild(notification);
        }, 300); // –í—Ä–µ–º—è –¥–ª—è –∞–Ω–∏–º–∞—Ü–∏–∏ –∏—Å—á–µ–∑–Ω–æ–≤–µ–Ω–∏—è
    }, 3000);
}

// –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è –ø—Ä–∏ –∑–∞–≥—Ä—É–∑–∫–µ —Å—Ç—Ä–∞–Ω–∏—Ü—ã
document.addEventListener('DOMContentLoaded', function() {
    initUserData();
    
    // –û–±—Ä–∞–±–æ—Ç—á–∏–∫ –¥–ª—è –∫–Ω–æ–ø–∫–∏ —Ä–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏–∏/–≤—Ö–æ–¥–∞
    const openRegisterBtn = document.getElementById('openRegister');
    const registerModal = document.getElementById('registerModal');
    const closeRegisterBtn = document.getElementById('closeRegister');
    
    if (openRegisterBtn && registerModal) {
        openRegisterBtn.addEventListener('click', function() {
            registerModal.style.display = 'flex';
        });
    }
    
    if (closeRegisterBtn && registerModal) {
        closeRegisterBtn.addEventListener('click', function() {
            registerModal.style.display = 'none';
        });
    }
    
    // –ó–∞–∫—Ä—ã—Ç–∏–µ –º–æ–¥–∞–ª—å–Ω–æ–≥–æ –æ–∫–Ω–∞ –ø—Ä–∏ –∫–ª–∏–∫–µ –≤–Ω–µ –µ–≥–æ
    window.addEventListener('click', function(e) {
        if (e.target == registerModal) {
            registerModal.style.display = 'none';
        }
    });
    
    // –û–±—Ä–∞–±–æ—Ç–∫–∞ —Ñ–æ—Ä–º—ã —Ä–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏–∏
    const registerForm = document.getElementById('registerForm');
    if (registerForm) {
        registerForm.addEventListener('submit', function(e) {
            e.preventDefault();
            
            const username = registerForm.username.value;
            const email = registerForm.email.value;
            const bonusChecked = registerForm.bonusCheckbox.checked;
            
            const userData = getUserData();
            userData.username = username;
            userData.email = email;
            userData.registered = new Date().toISOString();
            
            // –ï—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –≤—ã–±—Ä–∞–ª –±–æ–Ω—É—Å
            if (bonusChecked) {
                userData.credits += 500;
            }
            
            localStorage.setItem('userData', JSON.stringify(userData));
            
            // –û–±–Ω–æ–≤–ª—è–µ–º –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏–µ –∫—Ä–µ–¥–∏—Ç–æ–≤
            updateHeaderCredits();
            
            // –ó–∞–∫—Ä—ã–≤–∞–µ–º –º–æ–¥–∞–ª—å–Ω–æ–µ –æ–∫–Ω–æ
            registerModal.style.display = 'none';
            
            // –ü–æ–∫–∞–∑—ã–≤–∞–µ–º —É–≤–µ–¥–æ–º–ª–µ–Ω–∏–µ
            showNotification(`–ê–∫–∫–∞—É–Ω—Ç ${username} —É—Å–ø–µ—à–Ω–æ —Å–æ–∑–¥–∞–Ω!${bonusChecked ? ' +500 –∫—Ä–µ–¥–∏—Ç–æ–≤ –±–æ–Ω—É—Å!' : ''}`, 'success');
        });
    }
});

// –≠–∫—Å–ø–æ—Ä—Ç–∏—Ä—É–µ–º —Ñ—É–Ω–∫—Ü–∏–∏ –¥–ª—è –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è –≤ –¥—Ä—É–≥–∏—Ö —Ñ–∞–π–ª–∞—Ö
window.getUserData = getUserData;
window.updateCredits = updateCredits;
window.showNotification = showNotification;